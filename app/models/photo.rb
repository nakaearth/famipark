  ####################################################################
  ##################### コールバック群 ###############################
  ####################################################################

  ####################################################################
  ##################### インスタンスメソッド #########################
  ####################################################################
  def thumb_url
    image.thumb.url
  end

  def original_url
    image_url(:original)
  end
end
